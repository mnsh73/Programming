package SDet;

public class Swap_Two_numbers_with3rdvariable {

	public static void main(String[] args) 
	{
		int a = 10;
		int b = 30;
		System.out.println("a= "+a+"  b= "+b);
		int temp;
		temp= a;
		a=b;
		b=temp;
		System.out.println("after change");
		System.out.println("a= "+a+"  b= "+b);
		

	}

}
